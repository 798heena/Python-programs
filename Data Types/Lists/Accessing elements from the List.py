#Python program to demonstrate accessing of element from List
#Creating a List with the use of multiple values
List = ["Python","with","Pranshu"]

#Accessing an element from the list using index number
print("Accessing an element from the List")
print(List[0])
print(List[2])

#Creating a Multi-Dimentional List
#(By Nesting a list inside a List)
List = [['Python','with'],['Pranshu']]

#Accessing an element from the Multi-Dimentional list using index number
print("\nAccessing an element from the Multi-Dimentional list")
print(List[0][1])
print(List[1][0])