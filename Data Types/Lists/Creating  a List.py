#Python Program to demonstrate Creation of List
#Creating a List
List = []
print("Blank List: ")
print(List)

#Creating a List of numbers
List = [10,20,14]
print("\nList of numbers: ")
print(List)

#Creating a List of strings and accessing using index
List = ["Python","with","Pranshu"]
print("\nList items: ")
print(List[0])
print(List[2])

#Creating a multi-dimentional List
#(By Nesting a list inside a List)
List = [['Python','with'],['Pranshu']]
print("\nMulti-Dimentional List: ")
print(List)