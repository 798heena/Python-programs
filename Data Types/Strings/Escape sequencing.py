#Python Program for Escape Sequencing of String
String = '''I'm "Pranshu"'''
print("Initial String with use triple quotes: ")
print(String)

#Escaping Single Quote
String = 'I\'m "Pranshu"'
print("Escaping single quotes: ")
print(String)

#Escaping Double Quotes
String = "I'm \"Pranshu\""
print("Escaping single quotes: ")
print(String)

#Printing Paths with the use of Escape Sequences
String = "C:\\Python\\Pranshu\\"
print("\nEscaping backslashes: ")
print(String)