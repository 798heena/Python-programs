#Examples of Arithmetic Operator
a = 9
b = 4

#Addition of numbers
add = a + b

#Subtraction of numbers
sub = a - b

#Multiplication of numbers
mul = a * b

#Division(float) of numbers
div1 = a / b

#Division(floor) of numbers
div2 = a // b

#Modulus of both numbers
mod = a % b

#Power
p = a ** b

#print results
print(add)
print(sub)
print(mul)
print(div1)
print(div2)
print(mod)
print(p)